public class while_loop {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int i=0,sum=0;
        while(i<=10)
        {
            sum=sum+i;
            i++;
        }
        System.out.println("sum is "+ sum);
        
    }
    
}
